/**
 * 比較時間
 */
//alert("gg : "+  );

var compareDateTime = function( nowTime/*目前時間*/ , targetTime/*被比較的時間*/ )
{
	/* 回傳分鐘數 */
	return ((Date.parse(nowTime)).valueOf() - (Date.parse(targetTime)).valueOf())/1000/60 ;
}	